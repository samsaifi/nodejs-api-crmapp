


/* 200 OK:

Standard response for successful HTTP requests.
Example: Successful retrieval of data.
201 Created:

The request has been fulfilled, and a new resource has been created.
Example: Successful creation of a new user.
204 No Content:

The server successfully processed the request but there is no content to send back.
Example: Successful deletion of a resource.
400 Bad Request:

The server could not understand the request due to invalid syntax or missing parameters.
Example: Invalid request parameters.
401 Unauthorized:

The request lacks valid authentication credentials.
Example: Authentication failure.
403 Forbidden:

The server understood the request but refuses to authorize it.
Example: Access to a resource is forbidden.
404 Not Found:

The requested resource could not be found on the server.
Example: Attempt to access a non-existent endpoint.
405 Method Not Allowed:

The request method (GET, POST, PUT, DELETE, etc.) is not supported for the requested resource.
Example: Using the wrong HTTP method for an endpoint.
500 Internal Server Error:

A generic error message returned when an unexpected condition was encountered on the server.
Example: Unexpected server-side exception.
502 Bad Gateway:

The server, while acting as a gateway or proxy, received an invalid response from an upstream server.
Example: Issues with an external service.
503 Service Unavailable:

The server is not ready to handle the request. Common causes include maintenance, temporary overloading, or server issues.
Example: Temporary unavailability of a service.
504 Gateway Timeout:

The server, while acting as a gateway or proxy, did not receive a timely response from the upstream server.
Example: Timeout waiting for a response from an external service. */